// 전사(transcribe) 프롬프트 빌더. 세 가지 변형:
//   - legacy: 단일 파일 전체 전사 (api handleTranscribe / recover.html 호환)
//   - segment: 5분 단위 세그먼트 전사 (api handleTranscribeSegment)
//   - local: 로컬 CLI 전사 (scripts/process-recording-locally.js)
// 프롬프트 문구 자체는 기존 코드와 바이트 단위로 동일해야 함.

export function buildLegacyTranscribePrompt() {
  return `첨부된 한국어 회의 녹음을 정확히 전사하세요.

[규칙]
1. 들리는 내용을 누락 없이 옮겨 쓸 것
2. 발언자 구분은 하지 말고 발언 순서대로 작성
3. "음", "어" 같은 군더더기는 생략하되 실제 의미 있는 말은 모두 포함
4. 잘 안 들리는 구간은 [불분명] 으로 표시
5. 문장 단위로 줄바꿈하여 가독성 확보
6. 해설이나 요약 없이 들은 말만 옮겨 쓸 것
7. 같은 어절·음절이 3회 이상 반복되는 것으로 들리면 한 번만 적은 뒤 [불분명] 으로 표시하고 다음 발화로 넘어갈 것`;
}

export function buildSegmentTranscribePrompt({ segmentIndex, totalSegments, synonymHint = '' } = {}) {
  const part = totalSegments
    ? `이 오디오는 전체 회의 중 ${segmentIndex + 1}/${totalSegments} 번째 5분 구간입니다.`
    : `이 오디오는 전체 회의의 한 구간입니다.`;

  return `${part} 앞뒤 구간은 별도로 처리되니 이 구간만 정확히 한국어로 전사하세요.

[규칙]
1. 들리는 내용을 누락 없이 옮겨 쓸 것
2. 발언자 구분은 하지 말고 발언 순서대로 작성
3. "음", "어" 같은 군더더기는 생략하되 실제 의미 있는 말은 모두 포함
4. 잘 안 들리는 구간은 [불분명] 으로 표시
5. 문장 단위로 줄바꿈하여 가독성 확보
6. 해설이나 요약 없이 들은 말만 옮겨 쓸 것
7. 같은 어절·음절이 3회 이상 반복되는 것으로 들리면 한 번만 적은 뒤 [불분명] 으로 표시하고 다음 발화로 넘어갈 것
8. 구간 시작/끝에 별도 표시(타임스탬프 등) 추가하지 말 것 — 본문만 출력${synonymHint}`;
}

